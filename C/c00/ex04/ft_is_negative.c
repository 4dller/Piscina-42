#include <unistd.h>

void	ft_is_negative(int n)
{
	char	neg_num;

	if (n < 0)
	{
		neg_num = 'N';
		write (1, &neg_num, 1);
	}
	else
	{
		neg_num = 'P';
		write (1, &neg_num, 1);
	}
}
